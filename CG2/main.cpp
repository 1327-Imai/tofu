#pragma region//射影変換
	//単位行列を代入
	//constMapTransform->mat = XMMatrixIdentity();

	////平行投影変換行列の計算
	//constMapTransform->mat = XMMatrixOrthographicOffCenterLH(
	//	0.0f , WINDOW_WIDTH ,
	//	WINDOW_HEIGHT , 0.0f ,
	//	0.0f , 1.0f
	//);

